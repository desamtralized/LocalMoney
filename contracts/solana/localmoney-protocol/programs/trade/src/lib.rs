    /// Enhanced accept_trade with automatic profile updates
    pub fn accept_trade_with_profile_updates(
        ctx: Context<AcceptTradeWithProfile>,
        trade_id: u64,
        maker_contact: String,
    ) -> Result<()> {
        let trade = &mut ctx.accounts.trade;
        let clock = Clock::get()?;

        // First call the original accept_trade logic
        // Validate current state and transition
        require!(
            trade.state == TradeState::RequestCreated,
            ErrorCode::InvalidTradeState
        );

        validate_trade_state_transition(&trade.state, &TradeState::RequestAccepted)?;

        require!(
            maker_contact.len() <= 500,
            ErrorCode::ContactInfoTooLong
        );

        // Check if trade has expired
        if trade.expires_at > 0 && clock.unix_timestamp > trade.expires_at {
            let expired_state = TradeStateItem {
                actor: ctx.accounts.maker.key(),
                state: TradeState::RequestExpired,
                timestamp: clock.unix_timestamp,
            };
            trade.add_state_history(expired_state)?;
            return Err(ErrorCode::TradeExpired.into());
        }

        // Validate offer and maker
        let offer_data = ctx.accounts.offer.try_borrow_data()?;
        let offer: OfferAccount = OfferAccount::try_deserialize(&mut &offer_data[8..])?;

        require!(
            ctx.accounts.maker.key() == offer.owner,
            ErrorCode::Unauthorized
        );
        require!(
            offer.id == trade.offer_id,
            ErrorCode::OfferNotFound
        );
        require!(
            offer.state == OfferState::Active,
            ErrorCode::OfferNotActive
        );

        // Update trade state to accepted
        let accepted_state = TradeStateItem {
            actor: ctx.accounts.maker.key(),
            state: TradeState::RequestAccepted,
            timestamp: clock.unix_timestamp,
        };

        trade.add_state_history(accepted_state)?;
        trade.maker_contact = Some(maker_contact);

        // Update both buyer and seller profile statistics via CPI
        // Update seller (maker) active trades count
        let seller_cpi_program = ctx.accounts.profile_program.to_account_info();
        let seller_cpi_accounts = profile::cpi::accounts::UpdateProfile {
            profile: ctx.accounts.seller_profile.to_account_info(),
            owner: ctx.accounts.maker.to_account_info(),
        };
        let seller_cpi_ctx = CpiContext::new(seller_cpi_program.clone(), seller_cpi_accounts);
        profile::cpi::update_trade_stats(seller_cpi_ctx, TradeStatType::ActiveTrades, true)?;

        // Update buyer (taker) active trades count if different from seller
        if trade.buyer != trade.seller {
            let buyer_cpi_accounts = profile::cpi::accounts::UpdateProfile {
                profile: ctx.accounts.buyer_profile.to_account_info(),
                owner: if trade.buyer == ctx.accounts.maker.key() {
                    ctx.accounts.maker.to_account_info()
                } else {
                    // In this case, the buyer would be the original taker
                    // For now, we'll use the maker as it's the signer we have
                    ctx.accounts.maker.to_account_info()
                },
            };
            let buyer_cpi_ctx = CpiContext::new(seller_cpi_program, buyer_cpi_accounts);
            profile::cpi::update_trade_stats(buyer_cpi_ctx, TradeStatType::ActiveTrades, true)?;
        }

        msg!(
            "Trade accepted with profile updates: ID {}, maker: {}",
            trade_id,
            ctx.accounts.maker.key()
        );

        Ok(())
    }

